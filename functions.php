function enqueue_custom_scripts()
{
	wp_enqueue_script('waypoints', 'https://cdnjs.cloudflare.com/ajax/libs/waypoints/4.0.1/jquery.waypoints.min.js', array('jquery'), '4.0.1', true);
	wp_enqueue_script('custom-scripts', get_template_directory_uri() . '/assets/js/custom-scripts.js', array('jquery'), '1.0', true);

	// Pass the AJAX URL to script.js
	wp_localize_script('custom-scripts', 'ajaxpagination', array(
		'ajaxurl' => admin_url('admin-ajax.php')
	));
}
add_action('wp_enqueue_scripts', 'enqueue_custom_scripts');


function load_more_posts()
{
	$args = array(
		'post_type' => 'books',
		'posts_per_page' => 4,
		'paged' => $_POST['page'],
		'orderby' => 'date', // Order by post date
		'order' => 'ASC', // Set the order to ascending
	); ?>
	
		<?php $query = new WP_Query($args);

		if ($query->have_posts()) :
			while ($query->have_posts()) : $query->the_post(); 
			// $book_title             = get_field('book_title');
            $book_short_description = get_field('book_short_description');
            $author                 = get_field('author');
            $book_image             = get_field('book_image');
			?>
				<div class="grid-item">

					<?php if ($book_image) : ?>
						<img src="<?php echo $book_image; ?>" alt="">
					<?php endif; ?>
					<h2><?php the_title(); ?></h2>
					<p><?php echo esc_html($author); ?></p>
					<p><?php echo esc_html($book_short_description); ?></p>

				</div>
		<?php 
		wp_reset_postdata();	
	endwhile;
			
		else :
			// No more posts
			echo 'nomore';
		endif;

		die(); ?>
	
<?php }

add_action('wp_ajax_load_more_posts', 'load_more_posts');
add_action('wp_ajax_nopriv_load_more_posts', 'load_more_posts');


